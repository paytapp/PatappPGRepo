	private final String name;
	private final String code;

	private CountryCodes(String name, String code){
		this.name = name;
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public String getCode(){
		return code;
	}
	
	
	public static String getCountryName(String countryCode){
		CountryCodes countryType = CountryCodes.getCountry(countryCode);		
		if(countryType == null) {
			return "";
		}
		return countryType.getName();
	}

	public static CountryCodes getCountry(String countryCode){
		CountryCodes countryObj = null;
		if(null!=countryCode){
			for(CountryCodes country:CountryCodes.values()){
				if(countryCode.equals(country.getCode().toString())){
					countryObj=country;
					break;
				}
			}
		}
		return countryObj;
	}	
	public static CountryCodes getInstanceIgnoreCase(String name){
		CountryCodes[] countryTypes = CountryCodes.values();
		for(CountryCodes countryType : countryTypes){
			if(countryType.getName().equalsIgnoreCase(name)){
				return countryType;
			}
		}		
		return null;
	}
	public static String getCountryTypeName(String countryCode) {
		String countryType = null;
		if (null != countryCode) {
			for (CountryCodes mop : CountryCodes.values()) {
				if (countryCode.equals(mop.getName().toString())) {
					countryType = mop.getCode();
					break;
				}
			}
		}
		return countryType;
	}
